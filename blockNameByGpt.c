int main() {
   char name[100];
   printf("Enter a name: ");
   scanf("%s", name);

   // Convert to uppercase for simplicity
   for (int i = 0; name[i]; i++) {
       if (name[i] >= 'a' && name[i] <= 'z')
           name[i] -= 32;
   }

   printf("\n");
   // print line by line for all letters together
   for (int row = 0; row < 5; row++) {
       for (int k = 0; k < strlen(name); k++) {
           char ch = name[k];
           if (ch >= 'A' && ch <= 'Z')
               printf("%s  ", letters[ch - 'A'][row]);
       }
       printf("\n");
   }

   return 0;
}


